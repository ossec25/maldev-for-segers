        private static byte[] encryptionXOR(byte[] tableauText)
        {
            byte[] encrypte = new byte[tableauText.Length];
            for (int i = 0; i < tableauText.Length; i++)
            {
                //Xor opération : ^
                encrypte[i] = (byte)((uint)tableauText[i] ^ cle);
            }
            return encrypte;
        }
