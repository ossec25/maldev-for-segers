        private static void printTableauByte(string varname, byte[] data)
        {
            StringBuilder hexString = new StringBuilder(data.Length * 2);

            for (int count = 0; count < data.Length; count++)
            {
                byte b = data[count];
                if ((count + 1) == data.Length)
                {
                    hexString.AppendFormat("0x{0:x2}", b);
                }
                else
                {
                    hexString.AppendFormat("0x{0:x2}, ", b);
                }

                if ((count + 1) % 15 == 0)
                {
                    hexString.Append("\n");
                }
            }

            Console.WriteLine($"Tableau de byte[] {varname} = new byte[{data.Length}] {{\n{hexString}\n}};");

        }

